public void loadTable(){
    TableModel tm = jTable1.getModel();
        DefaultTableModel dtm = (DefaultTableModel) tm;
        dtm.setRowCount(0);
        
        DecimalFormat df = new DecimalFormat("#.00");
        jLabel24.setText("0.00");
          Double total = 0.00;
        
    try {
        ResultSet rs7 = DBMS.search("SELECT * FROM stock ORDER BY CAST(id AS unsigned) ASC");
        while (rs7.next()) {            
            String s_id = rs7.getString("id");
            String cate = "";
            ResultSet rs8 = DBMS.search("SELECT `name` FROM category WHERE `id`='"+rs7.getString("cate_id")+"'");
              if(rs8.next()){
                 cate = rs8.getString("name");
              }
            String name = rs7.getString("name");
            String weight = rs7.getString("weight");
            String sp = rs7.getString("sell_p");
            String b_q = rs7.getString("qty_i_b");
            
            Vector<String> v = new Vector<>();
            v.add(s_id);
            v.add(cate);
            v.add(name);
            v.add(weight);
            v.add(sp);
            v.add(b_q);
            Double d_qty = 0.00;
            ResultSet rs4 = DBMS.search("SELECT * FROM return_stock WHERE `stock_id`='"+s_id+"'");
            while (rs4.next()) {                
                String qty = rs4.getString("up_qty");
                d_qty += Double.valueOf(qty);
                Double d_sp = Double.valueOf(sp);
                Double d_qt = Double.valueOf(qty);
                Double amu = d_qt*d_sp;
                total += amu;
            }
                             Double y1 = Double.valueOf(b_q);
                             Double xy = d_qty/y1;
                             int xy1 = xy.intValue();
                             v.add(String.valueOf(xy1));
                             Double xy2 = Double.valueOf(xy1);
                             Double xy3 = xy2*y1;
                             Double xy4 = d_qty-xy3;
                             int xy5 = xy4.intValue();
                             v.add(String.valueOf(xy5));
                             v.add(String.valueOf(d_qty.intValue()));
              int x = d_qty.intValue();
            if(x!=0){
            dtm.addRow(v);
            }
        }
       jLabel24.setText(df.format(total));
    } catch (Exception e) {
    }
        
 }
